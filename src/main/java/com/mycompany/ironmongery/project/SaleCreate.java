    private void tbSCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSCMouseClicked
        // TODO add your handling code here:
        
         int fila = tbSC.getSelectedRow();
        int columna = tbSC.getSelectedColumn();

        double precio = Double.parseDouble(
            tbSC.getValueAt(fila, 2).toString()
        );

        int cantidad = Integer.parseInt(
            tbSC.getValueAt(fila, 4).toString()
        );

        // +
        if(columna == 5)
        {
            cantidad++;

            modeloCarrito.setValueAt(cantidad, fila, 4);
            modeloCarrito.setValueAt(precio * cantidad, fila, 6);
        }

        // -
        if(columna == 3)
        {
            cantidad--;

            if(cantidad <= 0)
            {
                modeloCarrito.removeRow(fila);
            }
            else
            {
                modeloCarrito.setValueAt(cantidad, fila, 4);
                modeloCarrito.setValueAt(precio * cantidad, fila, 6);
            }
        }
        
        
        if(columna == 7)
        {
            
            modeloCarrito.removeRow(fila);
        }
    }//GEN-LAST:event_tbSCMouseClicked

