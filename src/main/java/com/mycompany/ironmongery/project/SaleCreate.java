    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int fila = jTable1.getSelectedRow();

        if(fila == -1)
            return;

        int columna = jTable1.getSelectedColumn();

        // Columna DETALLES
        if(columna == 4)
        {
            int productoId = Integer.parseInt(
                jTable1.getValueAt(fila, 0).toString()
            );

            String producto =
                jTable1.getValueAt(fila, 1).toString();

            double precio = Double.parseDouble(
                jTable1.getValueAt(fila, 2).toString()
            );

            agregarCarrito(productoId, producto, precio);
        }
    }//GEN-LAST:event_jTable1MouseClicked

