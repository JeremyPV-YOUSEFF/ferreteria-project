    private void listarProductos(String buscar)
    {
        try
        {
            Conexion conexion = new Conexion();
            Connection cn = conexion.establecerConexion();

            String sql = """
             SELECT ProductoId,
                    Nombre,
                    Precio,
                    CASE
                        WHEN Estado = 1 THEN 'Activado'
                        ELSE 'Desactivado'
                    END AS Estado
             FROM Producto
             WHERE Nombre LIKE ?
             """;

            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setString(1, "%" + buscar + "%");

            ResultSet rs = ps.executeQuery();

            modelo.setRowCount(0);

            while(rs.next())
            {
                Object[] fila = new Object[5];

                fila[0] = rs.getInt("ProductoId");
                fila[1] = rs.getString("Nombre");
                fila[2] = rs.getBigDecimal("Precio");
                fila[3] = rs.getString("Estado");
                fila[4] = "Agregar";

                modelo.addRow(fila);
            }

            rs.close();
            ps.close();
            cn.close();
        }
        catch(Exception e)
        {
            System.out.println(e.getMessage());
        }
    }
    
    private void agregarCarrito(int productoId, String producto, double precio)
    {
        for(int i = 0; i < modeloCarrito.getRowCount(); i++)
        {
            String nombre =
                modeloCarrito.getValueAt(i, 1).toString();

            if(nombre.equals(producto))
            {
                int cantidad = Integer.parseInt(
                    modeloCarrito.getValueAt(i, 4).toString()
                );

                cantidad++;

                modeloCarrito.setValueAt(cantidad, i, 4);
                modeloCarrito.setValueAt(precio * cantidad, i, 6);

                return;
            }
        }

        modeloCarrito.addRow(new Object[]{
            productoId,
            producto,
            precio,
            "-",
            1,
            "+",
            precio,
            "Eliminar"
        });
    }
    

